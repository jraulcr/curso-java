public class UsoBucleFor {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		for(int i=0; i<10; i++) {			
			System.out.println("Juan " + i);			
		}
		
		System.out.println("\n");
		
		for(int j=10; j>0; j--) {			
			System.out.println("Juan " + j);			
		}
		
		System.out.println("\n");
		
		for(int z=0; z<20; z+=2) {			
			System.out.println("Juan " + z);			
		}
	}

}
